class WatchItem {
  var id;
  var description;
  var title;
  var imageUrl;
  var price;
  WatchItem({var id, var description, var title, var imageUrl, var price}) {
    this.id = id;
    this.description = description;
    this.title = title;
    this.imageUrl = imageUrl;
    this.price = price;
  }
}
